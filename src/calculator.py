"""
  Write a program that given two numbers as input make the main operations.
  Output:
  Insert first number: 4
  Insert second number: 2
  SUM: 6
  Difference: 2
  Multiplication: 8
  Division: 2
"""

print('Insert first number: ')
n=input()

print('Insert second number: ')
m=input()

print('Sum: ')
print(n+m)

print('Difference: ')
print(n-m)

print('Multiplication: ')
print(n*m)

print('Division: ')
print(n/m)
